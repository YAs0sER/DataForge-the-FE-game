function normalizeText(value) {
  return String(value)
    .replace(/\u00a0/g, ' ')
    .replace(/â€”/g, '—')
    .replace(/â€“/g, '–')
    .replace(/â€º/g, '›')
    .replace(/â€¹/g, '‹')
    .replace(/â†’/g, '→')
    .replace(/â†º/g, '↺')
    .replace(/Â·/g, '·')
    .replace(/â€™/g, '\'')
    .replace(/â€˜/g, '\'')
    .replace(/â€œ/g, '"')
    .replace(/â€/g, '"')
    .replace(/[’]/g, '\'')
    .replace(/[“”]/g, '"')
    .replace(/\s+/g, ' ')
    .trim();
}

const EXACT_FR = new Map(
  Object.entries({
    ...EXACT_FR_SOURCE,
    ...LEVEL_EXACT_FR_SOURCE,
    ...EXTRA_LEVEL_EXACT_FR_SOURCE,
    ...PROBLEM_EXACT_FR_SOURCE,
  })
    .map(([source, target]) => [normalizeText(source), target])
);

function preserveWhitespace(original, translated) {
  const leading = original.match(/^\s*/)?.[0] ?? '';
  const trailing = original.match(/\s*$/)?.[0] ?? '';
  return `${leading}${translated}${trailing}`;
}

function replaceInsensitive(text, source, replacement) {
  return text.replace(new RegExp(source.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi'), replacement);
}

export function translateText(value) {
  if (activeLanguage !== 'fr' || typeof value !== 'string') return value;

  const trimmed = normalizeText(value);
  if (!trimmed) return value;
  if (/^(?:[\d\s.,%$€+\-*/=<>()[\]{}|_:;'"`]+)$/.test(trimmed)) return value;
  if (/^(?:df|pd)(?:\[|\.)/.test(trimmed) || /^\w+\s*=\s*(?:df|pd)\b/.test(trimmed)) return value;

  const exact = EXACT_FR.get(trimmed);
  if (exact) return preserveWhitespace(value, exact);

  let translated = trimmed;
  const wordCount = trimmed.split(/\s+/).length;
  const allowShortPhraseFallback = wordCount <= 8;

  for (const [source, replacement] of PHRASES_FR) {
    if (!allowShortPhraseFallback && source.length < 18) continue;
    translated = replaceInsensitive(translated, source, replacement);
  }
  const allowWordFallback = wordCount <= 4 && !/[.!?]/.test(trimmed);
  if (allowWordFallback) {
    for (const [source, replacement] of WORDS_FR) {
      translated = translated.replace(new RegExp(`\\b${source}\\b`, 'gi'), replacement);
    }
  }

  return preserveWhitespace(value, translated);
}

function shouldSkip(element) {
  return !element || element.closest?.(SKIP_SELECTOR);
}

function translateNode(node) {
  if (node.nodeType === Node.TEXT_NODE) {
    if (shouldSkip(node.parentElement)) return;

    const current = node.nodeValue ?? '';
    if (translatedValues.get(node) === current) return;

    const translated = translateText(current);
    if (translated !== current) {
      node.nodeValue = translated;
      translatedValues.set(node, translated);
    }
    return;
  }

  if (!(node instanceof Element) || shouldSkip(node)) return;

  for (const attribute of ['placeholder', 'title', 'aria-label']) {
    if (!node.hasAttribute(attribute)) continue;
    const current = node.getAttribute(attribute) ?? '';
    const translated = translateText(current);
    if (translated !== current) node.setAttribute(attribute, translated);
  }

  const walker = document.createTreeWalker(node, NodeFilter.SHOW_TEXT);
  let textNode = walker.nextNode();
  while (textNode) {
    translateNode(textNode);
    textNode = walker.nextNode();
  }

  node.querySelectorAll?.('[placeholder], [title], [aria-label]').forEach(element => {
    if (shouldSkip(element)) return;

    for (const attribute of ['placeholder', 'title', 'aria-label']) {
      if (!element.hasAttribute(attribute)) continue;
      const current = element.getAttribute(attribute) ?? '';
      const translated = translateText(current);
      if (translated !== current) element.setAttribute(attribute, translated);
    }
  });
}

function startObserver() {
  observer?.disconnect();
  observer = new MutationObserver(mutations => {
    for (const mutation of mutations) {
      if (mutation.type === 'characterData') translateNode(mutation.target);
      mutation.addedNodes.forEach(translateNode);
    }
  });
  observer.observe(document.documentElement, { childList: true, subtree: true, characterData: true });
}

export function setLanguage(language = 'fr') {
  activeLanguage = SUPPORTED_LANGUAGES.has(language) ? language : 'fr';
  document.documentElement.lang = activeLanguage;
  document.documentElement.dataset.language = activeLanguage;
  observer?.disconnect();
  observer = null;

  if (activeLanguage === 'fr') {
    translateNode(document.body);
    startObserver();
  }

  window.dispatchEvent(new CustomEvent('dataforge:languagechange', { detail: { language: activeLanguage } }));
}

export function getLanguage() {
  return activeLanguage;
}

